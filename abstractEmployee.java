
abstract class abstractEmployee {
	
	public abstract void setFirst();
	public abstract void setLast();
	public abstract void setEmail();
	public abstract void setPhone();
	public abstract void setSalary();
	public abstract void setBonus();
	public abstract void setPay();
	public abstract void setOfficeLocation();
	
	public abstract String getFirst();
	public abstract String getLast();
	public abstract String getEmail();
	public abstract String getPhone();
	public abstract int getSalary();
	public abstract int getBonus();
	public abstract int getPay();
	public abstract String getOfficeLocation();
	
	
	
	public abstract void printInfo();
}

